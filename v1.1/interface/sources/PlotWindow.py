import sys
from PyQt4 import QtGui

from matplotlib.backends.backend_qt4agg import FigureCanvasQTAgg as FigureCanvas
from matplotlib.backends.backend_qt4agg import NavigationToolbar2QT as NavigationToolbar
import matplotlib.pyplot as plt

class PlotWindow(QtGui.QDialog):
    def __init__(self, parent=None, title = ""):
        super(PlotWindow, self).__init__(parent)
        self.setWindowTitle('Plot Window') 
        self.title = title
        

    def plot_signal(self, signal, columns, ylim = 0):
        # a figure instance to plot on
        self.figure = plt.figure()

        # this is the Canvas Widget that displays the `figure`
        # it takes the `figure` instance as a parameter to __init__
        self.canvas = FigureCanvas(self.figure)

        # this is the Navigation widget
        # it takes the Canvas widget and a parent
        self.toolbar = NavigationToolbar(self.canvas, self)

        # set the layout
        layout = QtGui.QVBoxLayout()
        layout.addWidget(self.toolbar)
        layout.addWidget(self.canvas)
        self.setLayout(layout)
        
        # create an axis
        ax = self.figure.add_subplot(111)
        ax.grid(True)
        ax.set_title(self.title)
        ax.set_xlabel(signal.index.name)
        
        if ylim != 0 : 
            ax.set_ylim(0, ylim)
        
        # plot data
        for data in columns :
            ax.plot(signal.index, signal[data], label=data)
        plt.legend(bbox_transform=plt.gcf().transFigure)
        
        # refresh canvas
        self.canvas.draw()
        self.show()
        
        
    def plot_figure(self, fig):
        # a figure instance to plot on
        self.figure = fig

        # this is the Canvas Widget that displays the `figure`
        # it takes the `figure` instance as a parameter to __init__
        self.canvas = FigureCanvas(self.figure)

        # this is the Navigation widget
        # it takes the Canvas widget and a parent
        self.toolbar = NavigationToolbar(self.canvas, self)

        # set the layout
        layout = QtGui.QVBoxLayout()
        layout.addWidget(self.toolbar)
        layout.addWidget(self.canvas)
        self.setLayout(layout)

        self.canvas = FigureCanvas(fig)

        # refresh canvas
        self.canvas.draw()
        self.show()
        